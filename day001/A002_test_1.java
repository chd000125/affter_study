package com.company.day001;

public class A002_test_1 {
	public static void main(String[] args) {
		System.out.println("좋아하는 색상은 RED입니다."); // printf("좋아하는 색상은 %s 입니다.", "red");
		System.out.println("이름 : 홍길동 \t 나이 : 25"); // printf("이름 : %s\t 나이 : %d", "홍길동", 12);
		System.out.println("A\nAB\nABC"); // printf("%s\n%s\n%s", "A", "AB", "ABC")
		System.out.println("I'm java :) " + 1.23); // 문자열 + 숫자 (출력)
		System.out.println(1+1 + "은 귀여미 1+1"); //1(숫자)+1(숫자) -> 계산
		System.out.println(1+1 + "은 귀요미 1"+1); //문자열 뒤에 있으면 출력
		System.out.printf("%s:%d살", "sally", 7);
	}
}	
