package com.company.day002;

public class A002 {
	public static void main(String[] args) {
		int a =10, b=3;
		System.out.println(a+b); //syso ctrl + space
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		//비교
		System.out.println(a>b);
		System.out.println(a<b);
		// 조건 &&(모든 조건) || (조건 중에 하나라도)
		System.out.println(a>b && a%3==0);
	}
}
